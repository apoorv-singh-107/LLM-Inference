"""
AI Endpoint Benchmarking Script
Evaluates SSE streaming endpoint: POST /generative_response

Brain tracking  : brain/text, brain/body events
Querifai tracking: brain/querifai/text, brain/querifai/body events
  - Captures every querifai invocation (brain may trigger querifai N times)
  - Within each invocation, captures ordered (tool_call → tool_result) steps
  - Deduplicates streaming artifacts using function-call IDs
  - Saves dedicated "Querifai Details", "Querifai Steps", and "SQL Comparison" sheets

SQL Comparison sheet: every SQL generated by querifai's execute_mysql_query calls
  is captured and compared against the expected SQL from slm_exploration.xlsx.
"""

