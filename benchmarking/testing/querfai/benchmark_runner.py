# ─────────────────────────────────────────────
#  DATA STRUCTURES
# ─────────────────────────────────────────────
@dataclass
class FunctionCall:
    """A single function/tool invocation captured from the stream."""

    agent: str  # "brain" | "querifai" | other sub-agent
    tool_name: str
    arguments: dict


@dataclass
class QuerifaiStep:
    """
    One complete tool call + its result within a querifai invocation.
    Maps to the call_id that querifai uses internally, so we can pair
    function_call ↔ function_response even across streaming chunks.
    """

    call_id: str  # e.g. "hb8iog4c" — from function_call.id
    step_index: int  # 0-based ordering within the invocation
    tool_name: str  # e.g. "get_tables", "execute_mysql_query"
    tool_args: dict  # arguments sent to the tool

    # Filled in when the matching function_response arrives
    tool_result: str = ""  # raw result string (SQL + table output, etc.)
    result_received: bool = False

    # Token cost at the moment this call was made (from usage in that chunk)
    prompt_tokens: int = 0
    completion_tokens: int = 0
    total_tokens: int = 0

    # Wall-clock offsets from t_start (seconds)
    call_received_at_sec: float = 0.0
    result_received_at_sec: float = 0.0


@dataclass
class QuerifaiInvocation:
    """
    One full querifai session, triggered by brain calling retrieval_augmented_generation.
    Brain may call querifai multiple times per question — each gets its own invocation.
    """

    invocation_index: int  # 0-based; increments each time brain re-triggers querifai
    triggered_at_sec: float  # seconds from t_start when first querifai chunk arrived

    steps: list = field(default_factory=list)  # list[QuerifaiStep], ordered

    # Filled in when brain/querifai/body fires
    final_response: str = ""  # assembled answer querifai produced
    body_received: bool = False
    completed_at_sec: float = 0.0

    # Cumulative tokens across all querifai chunks for this invocation
    total_prompt_tokens: int = 0
    total_completion_tokens: int = 0
    total_tokens: int = 0

    # ── Derived (computed after invocation completes) ──
    @property
    def tool_sequence(self) -> list[str]:
        """Ordered list of tool names called (only calls, not results)."""
        return [s.tool_name for s in self.steps]

    @property
    def step_count(self) -> int:
        return len(self.steps)

    @property
    def duration_sec(self) -> float:
        if self.completed_at_sec and self.triggered_at_sec:
            return round(self.completed_at_sec - self.triggered_at_sec, 3)
        return -1.0

    @property
    def steps_summary(self) -> str:
        """Human-readable ordered trace: call1 → result1 → call2 → result2 ..."""
        parts = []
        for s in self.steps:
            parts.append(f"[call] {s.tool_name}({_compact_args(s.tool_args)})")
            if s.result_received:
                preview = s.tool_result[:120].replace("\n", " ")
                parts.append(
                    f"[result] {preview}…"
                    if len(s.tool_result) > 120
                    else f"[result] {preview}"
                )
        return " → ".join(parts)


def _compact_args(args: dict) -> str:
    """One-line summary of tool args for display."""
    try:
        s = json.dumps(args, ensure_ascii=False)
        return s[:80] + "…" if len(s) > 80 else s
    except Exception:
        return str(args)[:80]


@dataclass
class BrainBodyEvent:
    """Parsed brain/body event."""

    response: Optional[str]
    function_calls: list
    prompt_tokens: int
    completion_tokens: int
    total_tokens: int
    raw_event_index: int


@dataclass
class BenchmarkResult:
    # ── Identity
    question_index: int
    question: str
    complexity: str

    # ── Timing
    request_sent_at: str
    total_time_sec: float
    time_to_first_brain_token_sec: float
    brain_generation_start_sec: float
    brain_generation_end_sec: float

